# Melakukan perulangan

# Looping dengan List
data_list = [0,3,5,7,8]

for i in data_list:
    print(f'data i adalah -> {i}')

print('Akhir Program 1 \n')

# Looping dengan range
for i in range(0,5):
    print(f'data i adalah -> {i}')

print('Akhir Program 2 \n')

# Looping string
data_string = 'masbel ganteng beuutzzz'

for i in data_string:
    print(f'{i}')

print('Akhir Program 3 \n')