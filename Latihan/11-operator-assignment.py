# Operator yang digunakan untuk menyingkat
'''
Contoh :
a = a + 1
Dapat ditulis menjadi :
a += 1
'''

# Operator aritmatika
a = 5
print('\nNilai a =',a)

a += 2
print('\nNilai a += 2 menjadi =',a)

a = 5
a -= 2
print('\nNilai a -= 2 menjadi =',a)

a = 5
a *= 2
print('\nNilai a *= 2 menjadi =',a)

a = 5
a /= 2
print('\nNilai a /= 2 menjadi =',a)

# Operasi modulus
a = 5
a %= 2
print('\nNilai a %= 2 menjadi =',a)

# Operasi Floor divisiod
a = 5
a //= 2
print('\nNilai a //= 2 menjadi =',a)

# Operasi dengan bitwise
a = True

# Operasi Logika
# OR
a |= False
print('\nNilai a =',a)
print('\nNilai a |= False menjadi =',a)

# AND
a = True
a &= False
print('\nNilai a &= False menjadi =',a)

# XOR
a = True
a ^= False
print('\nNilai a ^= False menjadi =',a)

# Shifting
a = 5
print('\nNilai a =',a)


# Left shifting
a <<= 1
print('\nNilai a <<= 1 menjadi =',a)

# Righ shifting
a = 5
a >>= 1
print('\nNilai a >>= 1 menjadi =',a)

